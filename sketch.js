function predict(high, low, volume, upOrDown){
    let bias = -5.0;
    // Four weights correspond to each input (high, low, volume, upOrDown)
    let weights = [0.10, 0.20, 0.06, 0.05];
    let z = bias + weights[0] * high + weights[1] * low + weights[2] * volume + weights[3] * upOrDown;
    return Math.round(1 / (1 + Math.pow(Math.E, -z)));
}

